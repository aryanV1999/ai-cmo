/**
 * Evidence-Based Recommendation Generator
 *
 * Takes collected evidence (business type, social analysis, brand positioning,
 * competitor data, findings) and uses GPT to generate specific,
 * evidence-grounded recommendations. Falls back to template-based
 * recommendations when GPT is unavailable.
 *
 * Principle: Every recommendation must cite specific evidence from the audit.
 * No generic advice like "improve your SEO" without a specific page or metric.
 */

import type { BusinessClassification } from "./business-type-classifier";

// ─────────────────────────────────────────
// TYPES
// ─────────────────────────────────────────

export interface RecommendationEvidenceSource {
  /** Category of evidence: business-type | social | brand | competitor | seo | technical */
  category: string;
  /** Specific evidence string */
  detail: string;
  /** Confidence in this evidence 0-100 */
  confidence?: number;
}

export interface EvidenceBackedRecommendation {
  id: string;
  title: string;
  description: string;
  priority: "CRITICAL" | "HIGH" | "MEDIUM" | "LOW";
  effort: "QUICK_WIN" | "MODERATE" | "SIGNIFICANT" | "MAJOR_PROJECT";
  category: "marketing" | "social" | "content" | "brand" | "seo" | "technical";
  /** The evidence that backs this recommendation */
  evidence: RecommendationEvidenceSource[];
  /** Expected impact when implemented */
  expectedImpact: string;
  /** Implementation steps */
  steps: string[];
  /** Whether this was generated by AI or template-based */
  generatedBy: "ai" | "template";
}

export interface RecommendationContext {
  domain: string;
  businessClassification?: BusinessClassification;
  brandPositioning?: {
    industryCategory?: string;
    businessModel?: string;
    valueProposition?: string;
    contentGaps?: string[];
    confidence?: number;
  };
  socialAnalysis?: {
    socialStrength?: number;
    platformCount?: number;
    confidence?: string;
    brandAlignment?: {
      status?: string;
      score?: number;
      evidenceLevel?: number;
    };
    recommendations?: string[];
    weaknesses?: string[];
    dataGaps?: string[];
  };
  competitors?: {
    count?: number;
    primaryGaps?: string[];
    quickWins?: string[];
    dataQuality?: string;
  };
  seoAndTechnical?: {
    criticalIssues?: number;
    warnings?: number;
    topIssues?: Array<{ title: string; severity: string }>;
  };
  pageCount?: number;
  hasBlog?: boolean;
  hasCaseStudies?: boolean;
  hasPricing?: boolean;
}

